package ex9_break_continue;

public class Ex2_Break {
public static void main(String[] args) {
	
	for (int i = 0; i < 3; i++) {
	
		switch (i) {
		case 0:
			System.out.println("0입니다");
			break;
		case 1 :
			System.out.println("1입니다");
			break;

		}// switch에 break가 있으면 스위치만 빠져나옴
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}//main
	
}
