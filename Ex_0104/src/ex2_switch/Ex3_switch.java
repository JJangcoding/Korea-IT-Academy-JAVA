package ex2_switch;

public class Ex3_switch {
public static void main(String[] args) {
	char ch = '이';
	
	
	//스위치문은 비교값으로 딱 세가지 타입만 사용이 가능
	//정수(byte,int,short), 문자열(String), 문자(char)  =>실수, 참거짓 불가능
	switch(ch) {
	case '김':
		System.out.println("21%");
		break;
	case '이':
		System.out.println("14%");
	    break;
	case '박':
		System.out.println("8%");
		break;
	
	}
	
	//break를 쓰지 않으면 break가 있을때까지 계속 실행(조건이 일치하지 않더라도)
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}//main
}
